frase = "               Bienvenidos a una nueva sesion del curso de python 3               "

print(frase)
print(frase.strip()) # Eliminar los espacions innecesarios al princion y al final del string
print(frase.rstrip()) # Eliminar los espacions innecesarios a la derecha al princion y al final del string
print(frase.lstrip()) # Eliminar los espacions innecesarios a la izquierda al princion y al final del string